package Day_2;

public class Division {

        public static void main(String[] args)
        {
            int a = 10;
            int b = 2;

            int id = a / b;
            float fd = (float)a / b;

            System.out.println("Integer division is " + id);
            System.out.println("Floating point division is " + fd);
        }
    }
